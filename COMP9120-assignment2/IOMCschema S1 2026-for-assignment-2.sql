    PRIMARY KEY (ATHLETE_ID, PAYMENT_DATE)
);

CREATE TABLE MEDICAL_PACKAGE (
    PACKAGE_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    PACKAGE_NAME VARCHAR(100) NOT NULL,
