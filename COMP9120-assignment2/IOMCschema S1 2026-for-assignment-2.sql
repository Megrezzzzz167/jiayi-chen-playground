CREATE TABLE OFFICIAL (
    OFFICIAL_ID INT PRIMARY KEY,
    USERNAME VARCHAR(50) NOT NULL UNIQUE,
    PASSWORD VARCHAR(100) NOT NULL,
    FIRSTNAME VARCHAR(100) NOT NULL,
    LASTNAME VARCHAR(100) NOT NULL,
    EMAIL VARCHAR(100) UNIQUE,
    MOBILE VARCHAR(20),
    SPECIALTY VARCHAR(50),
    PERFORMANCE TEXT
);

CREATE TABLE TRIALS (
    TRIAL_ID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TRIAL_NAME VARCHAR(100) NOT NULL,
    TRIAL_DATE DATE NOT NULL,
    TRIAL_TIME TIME NOT NULL DEFAULT '09:00:00',
    PERFORMANCE_DATA TEXT,
    UNIQUE (TRIAL_NAME, TRIAL_DATE)
);

CREATE TABLE PARTICIPATION (
    ATHLETE_ID INT REFERENCES ATHLETE (ATHLETE_ID) ON DELETE CASCADE,
    TRIAL_ID INT REFERENCES TRIALS (TRIAL_ID) ON DELETE CASCADE,
    OFFICIAL_ID INT NOT NULL REFERENCES OFFICIAL (OFFICIAL_ID),
    PARTICIPATION_DATE DATE NOT NULL,
    STATUS VARCHAR(20) NOT NULL DEFAULT 'scheduled',
    IS_WINNER BOOLEAN NOT NULL DEFAULT FALSE,
    PERFORMANCE_NOTE TEXT,
    PRIMARY KEY (ATHLETE_ID, TRIAL_ID),
    UNIQUE (ATHLETE_ID, PARTICIPATION_DATE),
    CONSTRAINT CHK_PARTICIPATION_STATUS CHECK (STATUS IN ('scheduled', 'completed'))
);

CREATE UNIQUE INDEX ONE_WINNER_PER_TRIAL
ON PARTICIPATION (TRIAL_ID)
WHERE IS_WINNER;

CREATE TABLE OVERSEES (
    OFFICIAL_ID INT REFERENCES OFFICIAL (OFFICIAL_ID) ON DELETE CASCADE,
    TRIAL_ID INT REFERENCES TRIALS (TRIAL_ID) ON DELETE CASCADE,
    PRIMARY KEY (OFFICIAL_ID, TRIAL_ID)
);

CREATE TABLE EQUIPMENT (
    AID INT REFERENCES ATHLETE (ATHLETE_ID) ON DELETE CASCADE,
    MANUFACTURER VARCHAR(100),
    CONDITION_STATUS VARCHAR(9) NOT NULL,
    NAME VARCHAR(100),
    MODEL VARCHAR(100),
    YEAR DATE,
    PRIMARY KEY (AID, NAME),
    CONSTRAINT CHK_CONDITION_STATUS CHECK (
        CONDITION_STATUS IN ('poor', 'fair', 'good', 'very good', 'excellent')
    )
);

CREATE TABLE SPONSORSHIP_PAYMENT (
    ATHLETE_ID INT REFERENCES ATHLETE (ATHLETE_ID) ON DELETE CASCADE,
    AMOUNT DECIMAL(10, 2) NOT NULL,
    PAYMENT_DATE DATE,
