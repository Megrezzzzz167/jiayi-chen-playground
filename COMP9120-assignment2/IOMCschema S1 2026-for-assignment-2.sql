    PACKAGE_ID INT REFERENCES MEDICAL_PACKAGE (PACKAGE_ID) ON DELETE CASCADE,
    PRIMARY KEY (ATHLETE_ID, PACKAGE_ID)
);

CREATE OR REPLACE FUNCTION itm_add_trial_participation(
    p_athlete_username VARCHAR,
    p_trial_name VARCHAR,
    p_trial_date DATE,
    p_official_username VARCHAR
)
RETURNS BOOLEAN
LANGUAGE plpgsql
AS $$
DECLARE
    v_athlete_id INT;
    v_official_id INT;
    v_trial_id INT;
BEGIN
    IF p_athlete_username IS NULL OR trim(p_athlete_username) = ''
       OR p_trial_name IS NULL OR trim(p_trial_name) = ''
       OR p_trial_date IS NULL
       OR p_official_username IS NULL OR trim(p_official_username) = '' THEN
        RETURN FALSE;
    END IF;

    SELECT ATHLETE_ID INTO v_athlete_id
    FROM ATHLETE
    WHERE lower(USERNAME) = lower(trim(p_athlete_username));

    SELECT OFFICIAL_ID INTO v_official_id
    FROM OFFICIAL
    WHERE lower(USERNAME) = lower(trim(p_official_username));

    IF v_athlete_id IS NULL OR v_official_id IS NULL THEN
        RETURN FALSE;
    END IF;

    IF EXISTS (
        SELECT 1
        FROM PARTICIPATION
        WHERE ATHLETE_ID = v_athlete_id
          AND PARTICIPATION_DATE = p_trial_date
    ) THEN
        RETURN FALSE;
    END IF;

    SELECT TRIAL_ID INTO v_trial_id
    FROM TRIALS
    WHERE lower(TRIAL_NAME) = lower(trim(p_trial_name))
      AND TRIAL_DATE = p_trial_date;

    IF v_trial_id IS NULL THEN
        INSERT INTO TRIALS (TRIAL_NAME, TRIAL_DATE, PERFORMANCE_DATA)
        VALUES (trim(p_trial_name), p_trial_date, trim(p_trial_name))
        RETURNING TRIAL_ID INTO v_trial_id;
    END IF;

    IF EXISTS (
        SELECT 1
        FROM PARTICIPATION
        WHERE ATHLETE_ID = v_athlete_id
          AND TRIAL_ID = v_trial_id
    ) THEN
        RETURN FALSE;
    END IF;

    INSERT INTO PARTICIPATION (ATHLETE_ID, TRIAL_ID, OFFICIAL_ID, PARTICIPATION_DATE, STATUS)
    VALUES (v_athlete_id, v_trial_id, v_official_id, p_trial_date, 'scheduled');

    INSERT INTO OVERSEES (OFFICIAL_ID, TRIAL_ID)
    VALUES (v_official_id, v_trial_id)
    ON CONFLICT DO NOTHING;

    RETURN TRUE;
EXCEPTION
    WHEN OTHERS THEN
        RETURN FALSE;
END;
$$;

CREATE OR REPLACE FUNCTION itm_update_trial_result(
    p_trial_id INT,
    p_athlete_username VARCHAR,
    p_is_winner BOOLEAN,
    p_official_username VARCHAR,
    p_performance_note TEXT
)
RETURNS BOOLEAN
LANGUAGE plpgsql
AS $$
DECLARE
    v_athlete_id INT;
    v_official_id INT;
    v_trial_date DATE;
BEGIN
    IF p_trial_id IS NULL
       OR p_athlete_username IS NULL OR trim(p_athlete_username) = ''
       OR p_official_username IS NULL OR trim(p_official_username) = '' THEN
        RETURN FALSE;
    END IF;

    SELECT ATHLETE_ID INTO v_athlete_id
    FROM ATHLETE
    WHERE lower(USERNAME) = lower(trim(p_athlete_username));

    SELECT OFFICIAL_ID INTO v_official_id
    FROM OFFICIAL
    WHERE lower(USERNAME) = lower(trim(p_official_username));

    SELECT TRIAL_DATE INTO v_trial_date
    FROM TRIALS
    WHERE TRIAL_ID = p_trial_id;

    IF v_athlete_id IS NULL OR v_official_id IS NULL OR v_trial_date IS NULL THEN
        RETURN FALSE;
    END IF;

    IF v_trial_date > CURRENT_DATE THEN
        RETURN FALSE;
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM PARTICIPATION
        WHERE TRIAL_ID = p_trial_id
          AND ATHLETE_ID = v_athlete_id
    ) THEN
        RETURN FALSE;
    END IF;

    IF p_is_winner AND EXISTS (
        SELECT 1
        FROM PARTICIPATION
        WHERE TRIAL_ID = p_trial_id
          AND ATHLETE_ID <> v_athlete_id
          AND IS_WINNER
    ) THEN
        RETURN FALSE;
    END IF;

    UPDATE PARTICIPATION
    SET IS_WINNER = p_is_winner,
        OFFICIAL_ID = v_official_id,
        PERFORMANCE_NOTE = NULLIF(trim(p_performance_note), ''),
        STATUS = 'completed'
    WHERE TRIAL_ID = p_trial_id
      AND ATHLETE_ID = v_athlete_id;

    INSERT INTO OVERSEES (OFFICIAL_ID, TRIAL_ID)
    VALUES (v_official_id, p_trial_id)
    ON CONFLICT DO NOTHING;

    RETURN TRUE;
EXCEPTION
    WHEN OTHERS THEN
        RETURN FALSE;
END;
$$;

